package stringDemo;

public class StringReplace {
	
	public static void main(String[] args) {
		
		String s = "selenium is for automation";
		
		String replaces = s.replace("for", "replacefor");
		System.out.println(replaces);
		
		String s1 = "selenium is for automation";
		
		String replacess = s1.replace("s", "k");
		System.out.println(replacess);
		
	}

}
