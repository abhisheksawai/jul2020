package basicJava;

public class IfDemo {
	
	public static void main(String[] args) {
		
		int age = 25;
		
		if(age > 20)  // if condition is true then it will execute that block
		{
			System.out.println("Yes age is greater than 20");
		}
		
		if(age < 20)  // if condition is false then it will NOT execute that block
		{
			System.out.println("Yes age is less than 20");
		}
		
	}

}
