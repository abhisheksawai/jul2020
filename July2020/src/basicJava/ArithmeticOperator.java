package basicJava;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
		
		// assignment operator
		int no1=10;
		int no2=20;
		
		//addition
		System.out.println(no1+no2); //30
		System.out.println(no2-no1); //20
		System.out.println("mul "+no1*no2);
		System.out.println("div "+no1/2);
		
		
	}

}
