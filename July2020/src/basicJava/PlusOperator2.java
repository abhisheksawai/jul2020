package basicJava;

public class PlusOperator2 {
	
	public static void main(String[] args) {
		
		// assignment operator
		int no;
		no = 10;
		System.out.println(no);  //10
		no++;
		System.out.println(no); // 11
		System.out.println(no++); // 11 is printing , and  12 would make it later then in next stm it will print
		System.out.println(no); // 12 - shishir
	}

}
